{
    [super viewDidLoad];
	// Do any additional setup after loading the view, typically from a nib.
    
    _peripheralManager = [[CBPeripheralManager alloc] initWithDelegate:self queue:nil];
    [self addDoneToolBarToKeyboard:_txtOutput];
}

